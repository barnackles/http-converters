package com.kodilla.kodillaconverter.domain;

public class MySemicolonCustomClass {

    private String fieldOne;
    private String fieldTwo;
    private int fieldThree;

    public MySemicolonCustomClass(String fieldOne, String fieldTwo, int fieldThree) {
        this.fieldOne = fieldOne;
        this.fieldTwo = fieldTwo;
        this.fieldThree = fieldThree;
    }

    public String getFieldOne() {
        return fieldOne;
    }

    public String getFieldTwo() {
        return fieldTwo;
    }

    public int getFieldThree() {
        return fieldThree;
    }
}
